const fs = require('fs');
const path = require('path');

const content = [
"const express = require('express');",
"const router = express.Router();",
"const { PrismaClient } = require('@prisma/client');",
"const prisma = new PrismaClient();",
"const { authenticate } = require('../middleware/authenticate');",
"router.get('/', authenticate, async (req, res) => {",
"  try {",
"    const collections = await prisma.collection.findMany({ where: { userId: req.user.id }, include: { cards: { include: { card: { include: { set: true } } } } } });",
"    res.status(200).json(collections);",
"  } catch (error) { res.status(500).json({ error: 'Failed to retrieve collections' }); }",
"});",
"router.get('/:id', authenticate, async (req, res) => {",
"  try {",
"    const collection = await prisma.collection.findUnique({ where: { id: parseInt(req.params.id) }, include: { cards: { include: { card: { include: { set: true } } } } } });",
"    if (!collection) return res.status(404).json({ error: 'Collection not found' });",
"    if (collection.userId !== req.user.id) return res.status(403).json({ error: 'Access denied' });",
"    res.status(200).json(collection);",
"  } catch (error) { res.status(500).json({ error: 'Failed to retrieve collection' }); }",
"});",
"router.post('/', authenticate, async (req, res) => {",
"  try {",
"    const { name } = req.body;",
"    const collection = await prisma.collection.create({ data: { name, userId: req.user.id } });",
"    res.status(201).json(collection);",
"  } catch (error) { res.status(500).json({ error: 'Failed to create collection' }); }",
"});",
"router.post('/:id/cards', authenticate, async (req, res) => {",
"  try {",
"    const { cardId, quantity, condition } = req.body;",
"    const collection = await prisma.collection.findUnique({ where: { id: parseInt(req.params.id) } });",
"    if (!collection) return res.status(404).json({ error: 'Collection not found' });",
"    if (collection.userId !== req.user.id) return res.status(403).json({ error: 'Access denied' });",
"    const collectionCard = await prisma.collectionCard.create({ data: { collectionId: parseInt(req.params.id), cardId: parseInt(cardId), quantity: parseInt(quantity), condition } });",
"    res.status(201).json(collectionCard);",
"  } catch (error) { res.status(500).json({ error: 'Failed to add card to collection' }); }",
"});",
"router.put('/:id', authenticate, async (req, res) => {",
"  try {",
"    const { name } = req.body;",
"    const collection = await prisma.collection.findUnique({ where: { id: parseInt(req.params.id) } });",
"    if (!collection) return res.status(404).json({ error: 'Collection not found' });",
"    if (collection.userId !== req.user.id) return res.status(403).json({ error: 'Access denied' });",
"    const updated = await prisma.collection.update({ where: { id: parseInt(req.params.id) }, data: { name } });",
"    res.status(200).json(updated);",
"  } catch (error) { res.status(500).json({ error: 'Failed to update collection' }); }",
"});",
"router.delete('/:id', authenticate, async (req, res) => {",
"  try {",
"    const collection = await prisma.collection.findUnique({ where: { id: parseInt(req.params.id) } });",
"    if (!collection) return res.status(404).json({ error: 'Collection not found' });",
"    if (collection.userId !== req.user.id) return res.status(403).json({ error: 'Access denied' });",
"    await prisma.collectionCard.deleteMany({ where: { collectionId: parseInt(req.params.id) } });",
"    await prisma.collection.delete({ where: { id: parseInt(req.params.id) } });",
"    res.status(200).json({ message: 'Collection deleted successfully' });",
"  } catch (error) { res.status(500).json({ error: 'Failed to delete collection' }); }",
"});",
"router.get('/sets/:setId/missing', authenticate, async (req, res) => {",
"  try {",
"    const setId = parseInt(req.params.setId);",
"    const allCards = await prisma.card.findMany({ where: { setId } });",
"    const userCollections = await prisma.collection.findMany({ where: { userId: req.user.id }, include: { cards: true } });",
"    const collectedCardIds = userCollections.flatMap(c => c.cards.map(cc => cc.cardId));",
"    const missingCards = allCards.filter(card => !collectedCardIds.includes(card.id));",
"    const ownedCards = allCards.filter(card => collectedCardIds.includes(card.id));",
"    res.status(200).json({ setId, totalCards: allCards.length, missingCount: missingCards.length, ownedCount: ownedCards.length, cards: [...missingCards.map(c => ({ ...c, collected: false })), ...ownedCards.map(c => ({ ...c, collected: true }))] });",
"  } catch (error) { res.status(500).json({ error: 'Failed to retrieve missing cards' }); }",
"});",
"module.exports = router;"
].join('\n');

fs.writeFileSync(path.join(__dirname, '../routes/collections.js'), content);
console.log('collections.js written successfully!');